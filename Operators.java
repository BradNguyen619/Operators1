public class Operators
{
    public static void main(String[] args)
    {
        int i ;
        double f = 2.5 ;
        boolean flag = true ;
        i = 10 ; {
            System.out.println(++i) ;
        }
        
        f += i ; {
            System.out.println(f) ;
        }
        flag = !flag ; {
            System.out.println(flag) ;
        }
    }
}
